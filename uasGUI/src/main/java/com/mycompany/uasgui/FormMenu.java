    private void jMenuItemHapusAkunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemHapusAkunActionPerformed
        // TODO add your handling code here:
        int konfirmasi = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus akun ini?\nData yang disimpan akan dihapus!",
                "Konfirmasi Hapus Akun",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            AkunPemancing akunManager = new AkunPemancing();
            if (akunManager.hapusAkun(currentUsername)) {
                JOptionPane.showMessageDialog(this, "Akun berhasil dihapus.");
                // Kembali ke Form Login
                FormLogin loginForm = new FormLogin();
                loginForm.setVisible(true);
                loginForm.pack();
                loginForm.setLocationRelativeTo(this);
                this.dispose(); // Menutup form saat ini
            } else {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus akun.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jMenuItemHapusAkunActionPerformed

