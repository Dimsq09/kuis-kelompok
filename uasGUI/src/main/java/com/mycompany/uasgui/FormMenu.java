    private void jMenuHapusBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuHapusBarangActionPerformed
        // TODO add your handling code here:
        FormHome formHome = (FormHome) this; // Casting ke FormHome
        String itemNameToDelete = formHome.getSelectedItemName(); // Ambil item yang dipilih

        if (itemNameToDelete != null) {
            int confirmation = JOptionPane.showConfirmDialog(this,
                    "Apakah Anda yakin ingin menghapus barang \"" + itemNameToDelete + "\"?",
                    "Konfirmasi Hapus Barang",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE);

            if (confirmation == JOptionPane.YES_OPTION) {
                Data dataHandler = new Data();
                boolean isDeleted = dataHandler.hapusData(currentUsername, itemNameToDelete);

                if (isDeleted) {
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                    formHome.refreshTable(); // Refresh tabel di FormHome
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus terlebih dahulu.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jMenuHapusBarangActionPerformed

